package in.ac.kletech.CALULATOR;

public class Calculator {
   int numSum(int iNUM1,int iNUM2){
	   return (iNUM1 +  iNUM2);
   }
   int numSub(int iNUM1,int iNUM2){
	   return (iNUM1 - iNUM2);
   }
   int numMul(int iNUM1,int iNUM2){
	   return (iNUM1 *  iNUM2);
   }
   int numdiv(int iNUM1,int iNUM2){
	   return (iNUM1 / iNUM2);
   }
}
